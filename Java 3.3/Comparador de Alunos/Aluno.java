public class Aluno {
	int numero;
	String nome;
	int anoMatricula;
	int anoNascimento;


	public int getNumber(){
		return numero;
	}

	public Aluno(String nome, int numero, int anoMatricula, int anoNascimento){
		this.nome = nome;
		this.numero = numero;
		this.anoMatricula = anoMatricula;
		this.anoNascimento = anoNascimento;
	}





}